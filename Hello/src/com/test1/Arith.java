package com.test1;

public class Arith {
	static int add(int num1, int num2) {
	
		return num1 + num2;
	}
	// Method Overload(메소드 중복정의)
	static double add(double num1, double num2) {
		
		return num1 + num2;
	}
	static int sub(int num1, int num2) {
		return num1 - num2;

	}
	static int mul(int num1, int num2) {
		return num1 * num2;

	}
	static int div(int num1, int num2) {
		return num1 / num2;

	}
}
